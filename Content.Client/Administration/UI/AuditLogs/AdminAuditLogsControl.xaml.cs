using System.Linq;
using Content.Shared.Administration.Logs;
using Content.Shared.Database;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Utility;
using static Robust.Client.UserInterface.Controls.BaseButton;
using static Robust.Client.UserInterface.Controls.LineEdit;

namespace Content.Client.Administration.UI.AuditLogs;

[GenerateTypedNameReferences]
public sealed partial class AdminAuditLogsControl : Control
{
    private static readonly Color RoutineColor = Color.White;
    private static readonly Color NotableColor = new(235, 200, 90);
    private static readonly Color CriticalColor = new(230, 90, 90);

    private readonly List<SharedAdminAuditLog> _loadedLogs = new();
    private readonly Dictionary<AdminAuditAction, Button> _actionButtons = new();

    private int _currentRound;
    private int _totalLogs;

    public AdminAuditLogsControl()
    {
        RobustXamlLoader.Load(this);

        RoundSpinBox.IsValid = i => i > 0 && i <= _currentRound;
        RoundSpinBox.InitDefaultButtons();
        RoundSpinBox.ValueChanged += RoundSpinBoxChanged;

        ResetRoundButton.OnPressed += ResetRoundPressed;

        ActionSearch.OnTextChanged += ActionSearchChanged;
        SelectAllActionsButton.OnPressed += SelectAllActions;
        SelectNoActionsButton.OnPressed += SelectNoActions;

        SearchLineEdit.OnTextChanged += SearchLineEditChanged;

        SearchModeKeyword.OnPressed += SearchModeKeywordPressed;
        SearchModeRegex.OnPressed += SearchModeRegexPressed;
        SearchModeWildcard.OnPressed += SearchModeWildcardPressed;
        SearchModeExact.OnPressed += SearchModeExactPressed;

        RoutineButton.OnPressed += SeverityButtonPressed;
        NotableButton.OnPressed += SeverityButtonPressed;
        CriticalButton.OnPressed += SeverityButtonPressed;

        SelectedSeverities.Add(AuditSeverity.Routine);
        SelectedSeverities.Add(AuditSeverity.Notable);
        SelectedSeverities.Add(AuditSeverity.Critical);

        SetActions(Enum.GetValues<AdminAuditAction>());
    }

    public int SelectedRoundId => RoundSpinBox.Value;
    public string Search => SearchLineEdit.Text.Trim();
    public LogSearchMode SelectedSearchMode { get; private set; } = LogSearchMode.Keyword;
    public IReadOnlyList<SharedAdminAuditLog> LoadedLogs => _loadedLogs;

    public HashSet<AuditSeverity> SelectedSeverities { get; } = new();
    public HashSet<AdminAuditAction> SelectedActions { get; } = new();
    public event Action? ActionFiltersChanged;

    public void SetMaxRound(int maxRoundId)
    {
        _currentRound = maxRoundId;

        if (RoundSpinBox.Value <= 0)
            RoundSpinBox.Value = maxRoundId;

        ResetRoundButton.Text = Loc.GetString("admin-logs-reset-with-id", ("id", maxRoundId));
        UpdateResetButton();
    }

    public void SetServerName(string name)
    {
        ServerNameLabel.Text = name;
    }

    public void UpdateCount(int count)
    {
        _totalLogs = count;
        UpdateCountLabel();
    }

    public void SetLogs(List<SharedAdminAuditLog> logs)
    {
        _loadedLogs.Clear();
        _loadedLogs.AddRange(logs);
        UpdateLogs();
    }

    public void AddLogs(List<SharedAdminAuditLog> logs)
    {
        _loadedLogs.AddRange(logs);
        UpdateLogs();
    }

    private void UpdateLogs()
    {
        LogsContainer.RemoveAllChildren();

        var shown = 0;
        foreach (var log in _loadedLogs)
        {
            if (!ShouldShowLog(log))
                continue;

            var label = new RichTextLabel
            {
                HorizontalExpand = true,
                Margin = new Thickness(2, 1, 2, 1)
            };

            label.SetMessage(FormatLog(log));
            LogsContainer.AddChild(label);
            shown++;
        }

        UpdateCountLabel(shown);
    }

    private bool ShouldShowLog(SharedAdminAuditLog log)
    {
        if (!SelectedSeverities.Contains(log.Severity))
            return false;

        if (!SelectedActions.Contains(log.Action))
            return false;

        if (string.IsNullOrWhiteSpace(Search))
            return true;

        var query = Search;

        if (log.Message.Contains(query, StringComparison.OrdinalIgnoreCase))
            return true;

        if (log.Action.ToString().Contains(query, StringComparison.OrdinalIgnoreCase))
            return true;

        if (log.TargetEntityName != null && log.TargetEntityName.Contains(query, StringComparison.OrdinalIgnoreCase))
            return true;

        if (log.TargetEntityPrototype != null && log.TargetEntityPrototype.Contains(query, StringComparison.OrdinalIgnoreCase))
            return true;

        return false;
    }

    private void SetActions(AdminAuditAction[] actions)
    {
        ActionTypesContainer.RemoveAllChildren();
        _actionButtons.Clear();
        SelectedActions.Clear();

        foreach (var group in actions.OrderBy(action => (int) action).GroupBy(GetActionGroup))
        {
            ActionTypesContainer.AddChild(new Label
            {
                Text = group.Key,
                Margin = new Thickness(2, 4, 2, 2)
            });

            foreach (var action in group)
            {
                var button = new Button
                {
                    Text = action.ToString(),
                    ToggleMode = true,
                    Pressed = true,
                    StyleClasses = { "ButtonSquare" }
                };

                button.OnPressed += _ => ActionButtonPressed(action, button);

                SelectedActions.Add(action);
                _actionButtons.Add(action, button);
                ActionTypesContainer.AddChild(button);
            }
        }
    }

    private static string GetActionGroup(AdminAuditAction action)
    {
        var value = (int) action;

        if (value < 20)
            return "Player Actions";
        if (value < 40)
            return "Entity Actions";
        if (value < 60)
            return "Round Actions";
        if (value < 70)
            return "Server Actions";
        if (value < 90)
            return "Moderation Actions";
        if (value < 100)
            return "Vote and Announcements";
        if (value < 110)
            return "Mapping Actions";
        if (value < 120)
            return "Admin State";
        if (value < 130)
            return "Commands";

        return "Resource Actions";
    }

    private FormattedMessage FormatLog(SharedAdminAuditLog log)
    {
        var msg = new FormattedMessage();

        msg.PushColor(new Color(150, 150, 150));
        msg.AddText($"[{log.OccurredAt:HH:mm:ss}] ");
        msg.Pop();

        msg.PushColor(new Color(100, 180, 255));
        msg.AddText(log.AdminUserName);
        msg.Pop();
        msg.AddText(" ");

        msg.PushColor(GetSeverityColor(log.Severity));
        msg.AddText($"[{log.Action}]");
        msg.Pop();

        if (log.TargetPlayerUserName != null || log.TargetEntityName != null)
        {
            msg.AddText(" -> ");
            msg.PushColor(new Color(255, 180, 100));
            if (log.TargetPlayerUserName != null)
                msg.AddText(log.TargetPlayerUserName);
            else if (log.TargetEntityName != null)
                msg.AddText(log.TargetEntityName);
            msg.Pop();
        }

        msg.AddText(" - ");
        msg.AddText(log.Message);

        return msg;
    }

    private static Color GetSeverityColor(AuditSeverity severity)
    {
        return severity switch
        {
            AuditSeverity.Critical => CriticalColor,
            AuditSeverity.Notable => NotableColor,
            _ => RoutineColor
        };
    }

    private void SeverityButtonPressed(ButtonEventArgs args)
    {
        SelectedSeverities.Clear();

        if (RoutineButton.Pressed)
            SelectedSeverities.Add(AuditSeverity.Routine);
        if (NotableButton.Pressed)
            SelectedSeverities.Add(AuditSeverity.Notable);
        if (CriticalButton.Pressed)
            SelectedSeverities.Add(AuditSeverity.Critical);

        UpdateLogs();
    }

    private void ActionButtonPressed(AdminAuditAction action, Button button)
    {
        if (button.Pressed)
            SelectedActions.Add(action);
        else
            SelectedActions.Remove(action);

        UpdateLogs();
        ActionFiltersChanged?.Invoke();
    }

    private void ActionSearchChanged(LineEditEventArgs args)
    {
        foreach (var (action, button) in _actionButtons)
        {
            button.Visible = action.ToString().Contains(ActionSearch.Text, StringComparison.OrdinalIgnoreCase);
        }
    }

    private void SearchLineEditChanged(LineEditEventArgs args)
    {
        UpdateLogs();
    }

    private void SetSearchMode(LogSearchMode mode)
    {
        SelectedSearchMode = mode;
        SearchModeKeyword.Pressed = mode == LogSearchMode.Keyword;
        SearchModeRegex.Pressed = mode == LogSearchMode.Regex;
        SearchModeWildcard.Pressed = mode == LogSearchMode.Wildcard;
        SearchModeExact.Pressed = mode == LogSearchMode.Exact;
    }

    private void SearchModeKeywordPressed(ButtonEventArgs args)
    {
        SetSearchMode(LogSearchMode.Keyword);
    }

    private void SearchModeRegexPressed(ButtonEventArgs args)
    {
        SetSearchMode(LogSearchMode.Regex);
    }

    private void SearchModeWildcardPressed(ButtonEventArgs args)
    {
        SetSearchMode(LogSearchMode.Wildcard);
    }

    private void SearchModeExactPressed(ButtonEventArgs args)
    {
        SetSearchMode(LogSearchMode.Exact);
    }

    private void SelectAllActions(ButtonEventArgs args)
    {
        SelectedActions.Clear();

        foreach (var (action, button) in _actionButtons)
        {
            button.Pressed = true;
            SelectedActions.Add(action);
        }

        UpdateLogs();
        ActionFiltersChanged?.Invoke();
    }

    private void SelectNoActions(ButtonEventArgs args)
    {
        SelectedActions.Clear();

        foreach (var button in _actionButtons.Values)
        {
            button.Pressed = false;
        }

        UpdateLogs();
        ActionFiltersChanged?.Invoke();
    }

    private void RoundSpinBoxChanged(ValueChangedEventArgs args)
    {
        UpdateResetButton();
    }

    private void ResetRoundPressed(ButtonEventArgs args)
    {
        RoundSpinBox.Value = _currentRound;
    }

    private void UpdateResetButton()
    {
        ResetRoundButton.Disabled = RoundSpinBox.Value == _currentRound;
    }

    private void UpdateCountLabel(int? shown = null)
    {
        var shownCount = shown ?? LogsContainer.ChildCount;
        CountLabel.Text = $"Showing {shownCount}/{_totalLogs}";
    }

    protected override void Dispose(bool disposing)
    {
        base.Dispose(disposing);

        RoundSpinBox.IsValid = null;
        RoundSpinBox.ValueChanged -= RoundSpinBoxChanged;
        ResetRoundButton.OnPressed -= ResetRoundPressed;

        ActionSearch.OnTextChanged -= ActionSearchChanged;
        SelectAllActionsButton.OnPressed -= SelectAllActions;
        SelectNoActionsButton.OnPressed -= SelectNoActions;
        SearchLineEdit.OnTextChanged -= SearchLineEditChanged;

        SearchModeKeyword.OnPressed -= SearchModeKeywordPressed;
        SearchModeRegex.OnPressed -= SearchModeRegexPressed;
        SearchModeWildcard.OnPressed -= SearchModeWildcardPressed;
        SearchModeExact.OnPressed -= SearchModeExactPressed;

        RoutineButton.OnPressed -= SeverityButtonPressed;
        NotableButton.OnPressed -= SeverityButtonPressed;
        CriticalButton.OnPressed -= SeverityButtonPressed;
    }
}
